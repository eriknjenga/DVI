	public function download_mos_trend($selected_year = 0, $national = "", $region = "", $district = "") {
		$year = date('Y');
		if ($selected_year != "0") {
			$year = $selected_year;
		}

		$counter = 2;
		$vaccine_objects = Vaccines::getAll();
		$population = 0;
		$title = "";
		if ($national > 0) {
			$title = "MOS Balance Trend at Central Vaccine Store";
